import UIKit

extension UIColor {
    
    public static var NHBlack: UIColor {
        return UIColor(named: "NH Black") ?? .black
    }
    
    public static var NHBlue: UIColor {
        return UIColor(named: "NH Blue") ?? .blue
    }
    
    public static var NHDarkGray: UIColor {
        return UIColor(named: "NH Dark Gray") ?? .darkGray
    }
    
    public static var NHDark: UIColor {
        return UIColor(named: "NH Dark") ?? .darkGray
    }
    
    public static var NHGreen: UIColor {
        return UIColor(named: "NH Green") ?? .green
    }
    
    public static var NHOrange: UIColor {
        return UIColor(named: "NH Orange") ?? .orange
    }
    
    public static var NHPink: UIColor {
        return UIColor(named: "NH Pink") ?? .systemPink
    }
    
    public static var NHRed: UIColor {
        return UIColor(named: "NH Red") ?? .red
    }
    
    public static var NHWhite: UIColor {
        return UIColor(named: "NH White") ?? .white
    }
    
}

